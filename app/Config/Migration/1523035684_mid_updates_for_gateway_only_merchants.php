	public function after($direction) {
		if ($direction = 'up') {
			$Merchant = $this->generateModel('Merchant');
			$updates = [];
			foreach ($this->midUpdates as $old => $new) {
				$id = $Merchant->field('id', ['merchant_mid' => $old]);
				if ($id) {
					$updates[] = [
						'id' => $id,
						'merchant_mid' => $new,
					];
				} else {
					echo "Merchant $old not found, skipping...\n";
				}
			}
			if (!empty($updates)) {
				//save without validation since we are simply updating
				$Merchant->saveMany($updates, ['validate' => false]);
			}
		}
		return true;
	}
